#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*d;
	const char	*s;

	d = (char *)dst;
	s = (const char *)src;
	if (dst == NULL && src == NULL)
		return (NULL);
	while (dst != src && n--)
		*d++ = *s++;
	return (dst);
}
